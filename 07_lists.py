# Lists in python 
a = [1,2,3,4,5]
print(a[0]) #to access the first element of the list/array
# similarly to get subsequent values 
# we write 
print(a[1])
print(a[2])
print(a[3])
print(a[4])

# To change the content of the list 
a[0] = 98 #this changes the first value of the lists 
print(a)

# We can create a list with items of different types 
c = [45, "soumya", True, 6.5] #Its correct syntax 
print(c)
