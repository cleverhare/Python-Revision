# Dictionary 
# Dictionary is collection of key value pair 
mydict = {
    "Fast": "coding",
    "Name": "Soumyajit",
    "Marks": [1,3,5],
    "anotherdict": {
        'soumya': 'coder'
    }
}
print(mydict['Fast']) #it will return coding 
print(mydict['Marks']) #it will return [1,3,5] 
print(mydict['anotherdict']['soumya']) #it will return coder

# Properties of dictionary in python 
# 1. It is un ordered 
# 2. It is mutable/ It can be changed 
# 3. It is indexed 
# 4. Can't contain duplicate keys 
